package ch.dkrieger.bansystem.lib.config.mode;

public enum ReasonMode {

    SELF(),
    TEMPLATE();

    public static ReasonMode parse(String name){
        try {
            return valueOf(name);
        }catch (Exception exception){}
        return TEMPLATE;
    }

}
